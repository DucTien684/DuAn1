    void setModel(KhachHang model) {
        txtmaKhachHang.setText(model.getMaKH());
        txttenKhachHang.setText(model.getTenKH());
       
        txtgioiTinh.setText(model.getGioiTinh());
      txtdiaChi.setText(model.getDiaChi());
         
         txtdienThoai.setText(model.getDienThoai());
    }

    KhachHang getModel() {
        KhachHang model = new KhachHang();
        model.setMaKH(txtmaKhachHang.getText());   
        model.setTenKH(txttenKhachHang.getText());
         model.setGioiTinh(txtgioiTinh.getText());
        model.setDiaChi(txtdiaChi.getText());
        model.setDienThoai(txtdienThoai.getText());
      
        
      
        return model;
    }

