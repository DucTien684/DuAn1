 void load() {
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0);
        try {
            List<KhachHang> list = KHdao.select();
            for (KhachHang kh : list) {
                Object[] row = {
                    kh.getMaKH(),
                    kh.getTenKH(),
                    kh.getGioiTinh(),
                    kh.getDiaChi(),
                    kh.getDienThoai()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

   

    void insert() {
        KhachHang model = getModel();  
        try {
            KHdao.insert(model);
            this.load();
            this.clear();
            DialogHelper.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại!");
            System.out.println(e);
        }
    }


    void update() {
        KhachHang model = getModel();
       try {
            KHdao.update(model);
            this.load();
            this.clear();
            DialogHelper.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "cập nhật thất bại");
            System.out.println(e);
        }
    }

    void delete() {
           if (DialogHelper.confirm(this, "Bạn thực sự muốn xóa khóa học này?")) {
           String maKH = txtmaKhachHang.getText();
            try {
                KHdao.delete(maKH);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Xóa thành công");
            } catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại");
            }
        }
    }

    void edit() {
        try {
            String maKH = (String) Table.getValueAt(this.index, 0);
            KhachHang model = KHdao.findById(maKH);
            if (model != null) {
                this.setModel(model);
                            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }
