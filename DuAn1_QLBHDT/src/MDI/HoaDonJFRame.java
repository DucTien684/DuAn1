     void load() {
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0);
        try {
            List<Hoadon> list = dao.select();
            for (Hoadon kh : list) {
                Object[] row = {
                    kh.getMaHD(),
                    kh.getMaKH(),
                    kh.getMaNV(),
                    kh.getNgayBan(),
                    kh.getTongTien()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

   

    void insert() {
        Hoadon model = getModel();  
        try {
            dao.insert(model);
            this.load();
            this.clear();
            DialogHelper.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại!");
            System.out.println(e);
        }
    }


    void update() {
        Hoadon model = getModel();
       try {
            dao.update(model);
            this.load();
            this.clear();
            DialogHelper.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "cập nhật thất bại");
            System.out.println(e);
        }
    }

    void delete() {
           if (DialogHelper.confirm(this, "Bạn thực sự muốn xóa khóa học này?")) {
           String maHD = txtmaHD.getText();
            try {
                dao.delete(maHD);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Xóa thành công");
            } catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại");
            }
        }
    }

    void edit() {
        try {
            String maHD = (String) Table.getValueAt(this.index, 0);
            Hoadon model = dao.findById(maHD);
            if (model != null) {
                this.setModel(model);
                            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }
