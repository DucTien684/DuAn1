    void setModel(Hoadon model) {
        txtmaHD.setText(model.getMaHD());
        txttenKH.setText(model.getMaKH());
       
        txttenNV.setText(model.getMaNV());
      txtNB.setText(model.getNgayBan());
         
        txtTT.setText(String.valueOf(model.getTongTien()));
    }

    Hoadon getModel() {
        Hoadon model = new Hoadon();
        model.setMaHD(txtmaHD.getText());   
        model.setMaKH(txttenKH.getText());
         model.setMaNV(txttenNV.getText());
        model.setNgayBan(txtNB.getText());
        model.setTongTien(Double.valueOf(txtTT.getText()));
      
        
      
        return model;
    }


